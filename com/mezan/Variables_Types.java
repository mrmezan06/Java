public class Variables_Types {
    public static void main(String[] args) {
        int a;
        char b;
        double c;
        boolean d;
        String myText = "Hello";

        a = 10;
        b = 'A';
        c = 19.99;
        float e = (float) c;
        d = true;

        System.out.println(myText+"A="+a +"B="+ b +"C="+ c +"D="+ d +"E="+ e);


    }
}
